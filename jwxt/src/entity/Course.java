package entity;

public class Course {

	private String	cour_no ;
	private int	cty_no ;
	private String	cour_name ;
	private double	cour_credit ;
	private int	cour_ctime ;
	private String	cour_open_team ;
	private String	cour_is_require ;
	private String	dept_no ;
	private String	cour_remark ;
	public String getCour_no() {
		return cour_no;
	}
	public void setCour_no(String cour_no) {
		this.cour_no = cour_no;
	}
	public int getCty_no() {
		return cty_no;
	}
	public void setCty_no(int cty_no) {
		this.cty_no = cty_no;
	}
	public String getCour_name() {
		return cour_name;
	}
	public void setCour_name(String cour_name) {
		this.cour_name = cour_name;
	}
	public double getCour_credit() {
		return cour_credit;
	}
	public void setCour_credit(double cour_credit) {
		this.cour_credit = cour_credit;
	}
	public int getCour_ctime() {
		return cour_ctime;
	}
	public void setCour_ctime(int cour_ctime) {
		this.cour_ctime = cour_ctime;
	}
	public String getCour_open_team() {
		return cour_open_team;
	}
	public void setCour_open_team(String cour_open_team) {
		this.cour_open_team = cour_open_team;
	}
	public String getCour_is_require() {
		return cour_is_require;
	}
	public void setCour_is_require(String cour_is_require) {
		this.cour_is_require = cour_is_require;
	}
	public String getDept_no() {
		return dept_no;
	}
	public void setDept_no(String dept_no) {
		this.dept_no = dept_no;
	}
	public String getCour_remark() {
		return cour_remark;
	}
	public void setCour_remark(String cour_remark) {
		this.cour_remark = cour_remark;
	}

	
}
