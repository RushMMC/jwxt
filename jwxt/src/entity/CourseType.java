package entity;

public class CourseType {

	private int ctyNo;
	private String ctyName;

	public CourseType() {
	}

	public int getCtyNo() {
		return ctyNo;
	}

	public void setCtyNo(int ctyNo) {
		this.ctyNo = ctyNo;
	}

	public String getCtyName() {
		return ctyName;
	}

	public void setCtyName(String ctyName) {
		this.ctyName = ctyName;
	}

}
