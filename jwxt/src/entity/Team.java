package entity;

public class Team {

	private Integer teamNo;
	private String teamYear;
	private String teamTeam;
	
	public Team() {
	}

	public Integer getTeamNo() {
		return teamNo;
	}

	public void setTeamNo(Integer teamNo) {
		this.teamNo = teamNo;
	}

	public String getTeamYear() {
		return teamYear;
	}

	public void setTeamYear(String teamYear) {
		this.teamYear = teamYear;
	}

	public String getTeamTeam() {
		return teamTeam;
	}

	public void setTeamTeam(String teamTeam) {
		this.teamTeam = teamTeam;
	}

}
